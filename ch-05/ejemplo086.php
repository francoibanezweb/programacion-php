<?php

$str = "59.6 minutos";

// Lo muestra en binario
printf("%b<br>",$str);

// Lo muestra según equivalencia en la tabla ASCII
printf("%c<br>",$str);

// Lo muestra en decimal
printf("%d<br>",$str);

// Lo muestra en decimal con coma
printf("%f<br>",$str);

// Lo muestra en octal
printf("%o<br>",$str);

// Lo muestra como string
printf("%s<br>",$str);

// Lo muestra en hexadecimal minúscula
printf("%x<br>",$str);

// Lo muestra en hexadecimal mayúsculas
printf("%X<br>",$str);

?>
